package array;

public class ArrayLinearElements {

	public static void main(String[] args) {
		int a[]=new int[5];
		a[0]=1;
		a[1]=3;
		a[2]=9;
		a[3]=4;
		a[4]=7;
		int n=4;
		for(int i=0;i<a.length;i++)
		{
		if(a[i]==n)
		{
			System.out.println(n+" is present in the array");
			break;
		}
	
		
	}
}
}
	
